// sin 

let sinValue = Math.sin(Math.PI / 2); 
console.log(sinValue);

//cos
let cosValue = Math.cos(Math.PI); // cos(180 degrees)
console.log(cosValue);

//tangent
let tanValue = Math.tan(Math.PI / 4); // tan(45 degrees)
console.log(tanValue);       

//Aresin: math.asin(x)
let arcsinValue = Math.asin(1); // arcsin(1)    
console.log(arcsinValue);


//Arccosine
let arccosValue = Math.acos(0); // arccos(0)
console.log(arccosValue);

//Arctangent
let atanValue = Math.atan(1); // atan(1)
console.log(atanValue);

//Arctangent
let atan2Value = Math.atan2(1, 1); // atan2(1, 1)
console.log(atan2Value);
